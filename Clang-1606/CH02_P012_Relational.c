#include <stdio.h>

int main()
{
    int a = 10;
    int b = 11;



    printf("Value of a is %d\n"  , a);
    printf("Value of b is %d\n"  , b);


    printf("a == b | %d\n", a == b);
    printf("a != b | %d\n", a != b);
    printf("a > b | %d\n", a > b);
    printf("a < b | %d\n", a < b);
    printf("a >= b | %d\n", a >= b);
    printf("a <= b | %d\n", a <= b);




}
