# Topic: Assignment Operators

x = 10
print("Initial x:", x)

x += 5        # x = x + 5
print("x += 5:", x)

x *= 2        # x = x * 2
print("x *= 2:", x)

x %= 7        # x = x % 7
print("x %= 7:", x)

# Other assignment operators
y = 20
y -= 4        # y = y - 4
print("y -= 4:", y)

y /= 2        # y = y / 2
print("y /= 2:", y)

y //= 3       # y = y // 3
print("y //= 3:", y)

y **= 2       # y = y ** 2
print("y **= 2:", y)
