# Topic: Working with Strings - Creating and Concatenation

# Creating strings
str1 = 'Hello'
str2 = "Python"
str3 = """This is
a multi-line
string"""

print(str1)
print(str2)
print(str3)

# String Concatenation using +
first_name = "Mary"
last_name = "Doe"
full_name = first_name + " " + last_name
print("Full Name:", full_name)

# Concatenation with type casting
age = 20
print("I am " + str(age) + " years old.")
