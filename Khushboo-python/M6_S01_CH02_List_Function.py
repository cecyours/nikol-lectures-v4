numbers = [1, 2, 3]

# append => The append() method adds an element to the end of the list.
numbers.append(8)

# insert => The insert() method adds an element at a specific position
numbers.insert(2,True)

# remove => Removes a specific element from the list.
numbers.remove(3)


# pop => Removes an element based on its index.
numbers.pop(2)




print(numbers)