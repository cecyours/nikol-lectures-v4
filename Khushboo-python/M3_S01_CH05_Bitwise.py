# Topic: Bitwise Operators

a = 5    # binary: 0101
b = 3    # binary: 0011

print("a & b:", a & b)     # AND  -> 0001 -> 1
print("a | b:", a | b)     # OR   -> 0111 -> 7
print("a ^ b:", a ^ b)     # XOR  -> 0110 -> 6
print("~a:", ~a)            # NOT  -> -6
print("a << 1:", a << 1)   # Left shift  -> 1010 -> 10
print("a >> 1:", a >> 1)   # Right shift -> 0010 -> 2
