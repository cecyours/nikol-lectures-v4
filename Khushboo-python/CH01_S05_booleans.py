is_active = True
is_deleted = False
print("is_active:", is_active, "| type:", type(is_active))
print("is_deleted:", is_deleted, "| type:", type(is_deleted))

# Comparisons return True/False
marks = 33
print("\nmarks:", marks)
print("marks > 40:", marks > 40)
print("marks >= 33:", marks >= 33)
print("marks == 33:", marks == 33)
print("marks != 33:", marks != 33)

# and / or / not
has_id = True
paid_fees = False
print("\nhas_id and paid_fees:", has_id and paid_fees)
print("has_id or paid_fees:", has_id or paid_fees)
print("not paid_fees:", not paid_fees)

